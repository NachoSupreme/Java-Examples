class ElectricGuitar {
	
	String brand;
	int numOfPickups;
	boolean rockStarUsesIt;
	//These are the instance variables or states of the class.
	//They define what the class knows


	String getBrand() {
		return brand;
	}

	void setBrand(String aBrand) {
		brand = aBrand;
	}

	int getNumOfPickups() {
		return numOfPickups;
	}
	void setNumOfPickups(int num) {
		numOfPickups = num;
	}
	boolean getRockStarUsesIt() {
		return rockStarUsesIt;
	}

	void setRockStarUsesIt(boolean yesOrNo) {
		rockStarUsesIt = yesOrNo;
	}
	//These are the methods of the class.
	//They define what the class does.
}

